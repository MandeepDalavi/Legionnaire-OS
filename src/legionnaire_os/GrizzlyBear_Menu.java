int quantity = (int) Quantity.getValue();
int total = Integer.parseInt(Amount.getText());
try
    {
        Class.forName("java.sql.DriverManager");
        Connection con = (Connection)
        DriverManager.getConnection("jdbc:mysql://localhost:3306/mandroid","root", "mandy15012001");
        Statement stmt = (Statement) con.createStatement();
        String query="INSERT INTO BILL VALUES('"+Food+"', "+price+", "+quantity+", "+total+");";
        stmt.executeUpdate(query);
    }
catch(Exception e)
    {
           JOptionPane.showMessageDialog(null, "Error in Connectivity!");
    }
